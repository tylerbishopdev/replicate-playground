// Group models by owner for better organization
export const GROUPED_MODELS = DEFAULT_MODELS.reduce((acc, model) => {
  if (!acc[model.owner]) {
    acc[model.owner] = [];
  }
  acc[model.owner].push(model);
  return acc;
}, {} as Record<string, ReplicateModel[]>);

// Get model display name (shortened for sidebar)
export const getModelDisplayName = (model: ReplicateModel): string => {
  return model.name
    .replace(/^flux-/, "")
    .replace(/^gen4-/, "")
    .replace(/^veo-/, "");
};
